package za.ac.cput.calculator;

/**
 *
 * @author Azania
 */
public class Addition {
    /**
     * This adds two integers
     * @param a
     * @param b
     * @return 
     */
    public static int sum(final int a, final int b){
        return a + b;
                }
    /**
     * This adds two really numbers.
     * @param a
     * @param b
     * @return 
     */
    public static double sum(double a, double b){
        return a + b;
    }
     }

